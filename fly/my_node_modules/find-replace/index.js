/*global require, module, Buffer */
var through = require('through2'),
	gutil = require('gulp-util');

var PLUGIN_NAME = 'reg-exp-code-cleaner';

var config = {
	defaults: {
		replace: '',
		find: ''
	}
};

module.exports = function (options) {

	"use strict";

	return through.obj(function (file, encoding, callback) {

		if (file.isNull()) {
			return callback(null, file);
		}

		if (file.isStream()) {
			return callback(new gutil.PluginError(PLUGIN_NAME, 'doesn\'t support Streams'));
		}

		var filesString = file.contents.toString(),
			defaultFind = config.defaults.find,
			defaultReplace = config.defaults.replace;

		// work with file here
		options.list.forEach(function (data) {
			filesString = filesString.replace(data.find || defaultFind, data.replace || defaultReplace);
		});

		file.contents = new Buffer(filesString, encoding);

		callback(null, file);

	});

};
